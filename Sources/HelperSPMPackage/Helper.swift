import Foundation

public class Helper {
    
    /// Perform an addition of the provided parameters
    /// - Parameters:
    ///   - a: First parameter to add
    ///   - b: Second parameter to add
    /// - Returns: return the addision of two parameter
    public static func sum(a: Int, b: Int) -> Int {
        a + b
    }
}
